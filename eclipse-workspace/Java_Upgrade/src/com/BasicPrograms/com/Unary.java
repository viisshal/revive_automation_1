package com.BasicPrograms.com;

public class Unary {
	public static void main(String args[]) {
		int a =5;
		int b =5;
		int c = -2;
		int d = 2;
		boolean e = true;
		boolean f = false;
		System.out.println(a--);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(++b - ++b);
		System.out.println(b++ - ++b);
		System.out.println(~c);//4
		System.out.println(~d);//3
		System.out.println(!e);
		System.out.println(!f);
		}
		
		
		
		}


