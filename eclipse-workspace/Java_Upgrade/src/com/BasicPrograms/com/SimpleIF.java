package com.BasicPrograms.com;

public class SimpleIF {
	public static void main(String args[]) {
		float a = 5.4f;
		int b= 4;
		if(a>6) {
			System.out.println("a is greater than 3.2");
		}
		else {
			System.out.println("a is not greater than 3.2");
		}
		if(b<3) {
			System.out.println("b is not less than 3");
		}
		else {
			System.out.println("b is greater than 3");
		}
		
	}

}
