package com.skills.com;

public class ArithmeticOperators {
	public static void main(String args[]) {
		int a = 2;
		int b = 3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println((10*10/5+3-1*4/2));
		System.out.println(2/1*5+3/2-4+3);
		
	}

}
